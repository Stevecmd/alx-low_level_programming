#include "main.h"
/**
 * _memcpy - Entry point of value
 * @dest: destination to copy input to
 * @src: source of input
 * @n: number of bytes from memory area
 * Return: Always 0 (Success)
 */

 char *_memcpy(char *dest, char *src, unsigned int)
 {
    unsigned int i = 0;

    for (i = 0; i < n; i++)
        dest[i] = src[i];
    return (dest);
 }